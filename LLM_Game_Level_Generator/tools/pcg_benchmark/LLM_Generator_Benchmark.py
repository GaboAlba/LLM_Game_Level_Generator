def save_results(results: dict, output_file: str):
    """
    Save benchmark results to a JSON file.

    Args:
        results: Benchmark results dictionary
        output_file: Path to output JSON file
    """
    try:
        output_path = Path(output_file)
        output_path.parent.mkdir(parents=True, exist_ok=True)

        with open(output_file, 'w') as f:
            json.dump(results, f, indent=2)

        print(f"Results saved to: {output_file}")
    except Exception as e:
        print(f"ERROR: Failed to save results: {e}")
        sys.exit(1)


def main():
    parser = argparse.ArgumentParser(
        description="Run PCG benchmark on LLM-generated game levels"
    )
    parser.add_argument(
        "--iteration",
        type=int,
        required=True,
        help="Current iteration number"
    )
    parser.add_argument(
        "--output",
        type=str,
        required=True,
        help="Output JSON file path for results"
    )
    parser.add_argument(
        "--level-file",
        type=str,
        default=None,
        help="Path to generated level file (optional, for testing)"
    )

    args = parser.parse_args()

    print("=" * 60)
    print(f"LLM Generator Benchmark - Iteration {args.iteration}")
    print("=" * 60)

    # For now, use a placeholder level if no file is provided
    # In production, this should call the LLM generator or read from a specific location
    if args.level_file:
        level_content = load_generated_level(args.level_file)
    else:
        print("WARNING: No level file provided, using placeholder")
        level_content = """##########
#........#
#.W......#
#........#
#......P.#
##########"""

    # Run the benchmark
    results = run_benchmark(level_content, args.iteration)

    # Save results
    save_results(results, args.output)

    # Exit with success if no errors, otherwise fail
    if "error" in results:
        print(f"\nIteration {args.iteration} FAILED")
        sys.exit(1)
    else:
        print(f"\nIteration {args.iteration} COMPLETED")
        print(f"Overall Score: {results['metrics']['overall_score']}")
        sys.exit(0)


if __name__ == "__main__":
    main()
