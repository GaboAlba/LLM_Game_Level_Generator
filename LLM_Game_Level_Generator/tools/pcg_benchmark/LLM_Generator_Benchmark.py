def load_generated_level(level_file: str) -> str:
    """
    Load a generated level from a file.

    Args:
        level_file: Path to the level file

    Returns:
        Level string content
    """
    try:
        with open(level_file, 'r') as f:
            return f.read()
    except FileNotFoundError:
        print(f"ERROR: Level file not found: {level_file}")
        sys.exit(1)
    except Exception as e:
        print(f"ERROR: Failed to read level file: {e}")
        sys.exit(1)


